def _add_draft_header_footer(doc: Document) -> None:
    """Add 'DRAFT — NOT LEGAL ADVICE' header and footer to every page."""
    for section in doc.sections:
        # Header
        header = section.header
        header.is_linked_to_previous = False
        hp = header.paragraphs[0] if header.paragraphs else header.add_paragraph()
        hp.alignment = WD_ALIGN_PARAGRAPH.CENTER
        hr = hp.add_run("DRAFT — NOT LEGAL ADVICE — AI-GENERATED RESEARCH ONLY")
        hr.bold = True
        hr.font.size = Pt(8)
        hr.font.color.rgb = RGBColor(176, 176, 176)
        hr.font.name = "Calibri"
        # Footer
        footer = section.footer
        footer.is_linked_to_previous = False
        fp = footer.paragraphs[0] if footer.paragraphs else footer.add_paragraph()
        fp.alignment = WD_ALIGN_PARAGRAPH.CENTER
        fr = fp.add_run("Generated by PatentForge · Not legal advice · Consult a patent attorney before acting")
        fr.font.size = Pt(7)
        fr.font.color.rgb = RGBColor(176, 176, 176)
        fr.font.name = "Calibri"


def export_docx(req: ExportRequest) -> bytes:
    """Generate a Word document formatted per USPTO 37 CFR 1.52."""
    doc = Document()
    _setup_page(doc)
    _add_draft_header_footer(doc)

    # ── Title Page ──────────────────────────────────────────────────────
    title_para = doc.add_paragraph()
    title_para.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = title_para.add_run(req.title or "Patent Application")
    run.bold = True
    run.font.name = "Times New Roman"
    run.font.size = Pt(18)

    subtitle = doc.add_paragraph()
    subtitle.alignment = WD_ALIGN_PARAGRAPH.CENTER
    sub_run = subtitle.add_run("Patent Application")
    sub_run.font.name = "Times New Roman"
    sub_run.font.size = Pt(14)

    date_para = doc.add_paragraph()
    date_para.alignment = WD_ALIGN_PARAGRAPH.CENTER
    d_run = date_para.add_run(f"Generated: {datetime.now().strftime('%Y-%m-%d')}")
    d_run.font.name = "Times New Roman"
    d_run.font.size = Pt(12)

    disclaimer = doc.add_paragraph()
    disclaimer.alignment = WD_ALIGN_PARAGRAPH.CENTER
    disc_run = disclaimer.add_run(
        "\nIMPORTANT: This is an AI-generated research draft. "
        "It must be reviewed by a registered patent attorney before filing."
    )
    disc_run.italic = True
    disc_run.font.name = "Times New Roman"
    disc_run.font.size = Pt(9)

    doc.add_page_break()

    # ── Sequential paragraph counter ────────────────────────────────────
    para_num = 1

    # Sections that receive [NNNN] paragraph numbering
    numbered_sections: list[tuple[str, str]] = [
        ("Background of the Invention", req.background or ""),
        ("Summary of the Invention", req.summary or ""),
        ("Brief Description of the Drawings", req.figure_descriptions or ""),
    ]

    for heading, content in numbered_sections:
        if not content.strip():
            continue
        _add_section_heading(doc, heading)
        for para_text in _paragraphs_from_content(content):
            para_num = _add_numbered_paragraph(doc, para_text, para_num)

    # ── Detailed Description (page break, paragraph numbered) ──────────
    dd_content = (req.detailed_description or "").strip()
    if dd_content:
        doc.add_page_break()
        _add_section_heading(doc, "Detailed Description of Preferred Embodiments")
        for para_text in _paragraphs_from_content(dd_content):
            para_num = _add_numbered_paragraph(doc, para_text, para_num)

    # ── Claims (page break, NOT paragraph numbered) ────────────────────
    claims_content = (req.claims or "").strip()
    if claims_content:
        doc.add_page_break()
        _add_section_heading(doc, "Claims")
        for para_text in _paragraphs_from_content(claims_content):
            p = doc.add_paragraph()
            _add_formatted_runs(p, para_text)

    # ── Abstract (page break, NOT paragraph numbered) ──────────────────
    abstract_content = (req.abstract or "").strip()
    if abstract_content:
        doc.add_page_break()
        _add_section_heading(doc, "Abstract of the Disclosure")
        # Abstract is a single paragraph (strip existing numbers)
        clean = _strip_existing_numbers(abstract_content).strip()
        p = doc.add_paragraph()
        _add_formatted_runs(p, clean)

    # ── Information Disclosure Statement (if present) ──────────────────
    ids_content = (req.ids_table or "").strip()
    if ids_content:
        _add_section_heading(doc, "Information Disclosure Statement")
        for para_text in ids_content.split("\n\n"):
            text = para_text.strip()
            if text:
                p = doc.add_paragraph()
                _add_formatted_runs(p, text)

    # ── Cross-References (if present) ──────────────────────────────────
    xref_content = (req.cross_references or "").strip()
    if xref_content:
        _add_section_heading(doc, "Cross-Reference to Related Applications")
        for para_text in _paragraphs_from_content(xref_content):
            p = doc.add_paragraph()
            _add_formatted_runs(p, para_text)

    # ── Page numbers + footer disclaimer ───────────────────────────────
    _add_page_numbers(doc)

    # ── Watermark ──────────────────────────────────────────────────────
    _add_watermark(doc)

    # Serialize
    buf = io.BytesIO()
    doc.save(buf)
    return buf.getvalue()


def export_markdown(req: ExportRequest) -> str:
    """Generate a Markdown document from the application sections."""
    parts = []

    parts.append(f"# {req.title or 'Patent Application'}\n")
    parts.append(f"*Generated: {datetime.now().strftime('%Y-%m-%d')}*\n")
    parts.append("*AI-generated research draft — must be reviewed by a registered patent attorney.*\n")

    sections = [
        ("Cross-Reference to Related Applications", req.cross_references),
        ("Background of the Invention", req.background),
        ("Summary of the Invention", req.summary),
        ("Detailed Description of Preferred Embodiments", req.detailed_description),
        ("Claims", req.claims),
        ("Abstract of the Disclosure", req.abstract),
        ("Brief Description of the Drawings", req.figure_descriptions),
        ("Information Disclosure Statement", req.ids_table),
    ]

    for heading, content in sections:
        if not content or not content.strip():
            continue
        parts.append(f"\n## {heading}\n")
        parts.append(content.strip())
        parts.append("")

    return "\n".join(parts)
